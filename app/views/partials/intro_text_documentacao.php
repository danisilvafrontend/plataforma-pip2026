    <h3 class="fw-bold">
      <i class="bi bi-file-earmark-lock me-2"></i> Documentação obrigatória do negócio
    </h3>

    <p>
      Para <strong>enviar seu negócio para aprovação</strong> na vitrine da Plataforma Impactos Positivos,
      é necessário anexar duas <strong>certidões oficiais</strong> que comprovam a 
      <strong>regularidade trabalhista e ambiental</strong> da sua organização.
    </p>

    <p>
      A <strong>Certidão Negativa de Débitos Trabalhistas (CNDT)</strong> comprova que o CNPJ do seu negócio 
      <strong>não possui pendências trabalhistas</strong> registradas na <strong>Justiça do Trabalho</strong>. 
      Essa certidão é emitida de forma <strong>gratuita</strong> e online pelo 
      <strong>Tribunal Superior do Trabalho (TST)</strong>.
      Você pode emitir a CNDT diretamente no portal oficial do TST:
      <a href="https://www.tst.jus.br/certidao1" target="_blank" rel="noopener noreferrer">
        https://www.tst.jus.br/certidao1
      </a>.
    </p>

    <p>
      A <strong>Certidão de Regularidade Ambiental</strong> demonstra que o negócio está 
      <strong>em conformidade com a legislação ambiental</strong>, sem <strong>multas ou sanções ativas</strong> 
      junto ao <strong>órgão ambiental competente</strong> (estadual ou federal). 
      Em muitos casos, ela é obtida por meio dos sistemas de <strong>licenciamento ambiental</strong> do seu estado
      ou, quando aplicável, via Cadastro Técnico Federal do IBAMA (CTF/APP), disponível em:
      <a href="https://servicos.ibama.gov.br/sicafiext/sistema.php" target="_blank" rel="noopener noreferrer">
        https://servicos.ibama.gov.br/sicafiext/sistema.php
      </a>.
    </p>

    <p>
      Essas <strong>documentações</strong> reforçam o compromisso com 
      <strong>boas práticas trabalhistas</strong> e <strong>responsabilidade socioambiental</strong>, 
      critérios essenciais para a <strong>curadoria</strong> e <strong>aprovação</strong> na vitrine da plataforma.
    </p>

    <p class="mb-0">
      Caso ainda não possua as certidões, recomendamos que faça a solicitação com antecedência, 
      garantindo que estejam <strong>válidas</strong> no momento da <strong>análise do seu negócio</strong>.
    </p>

